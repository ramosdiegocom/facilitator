export async function* generateTokens(prompt: string): AsyncGenerator<string> {
	const words = [
		"The",
		" question",
		" you",
		" asked",
		'--"',
		prompt,
		'"--is',
		" a",
		" fascinating",
		" one.",
		"\n\n",
		"In",
		" short,",
		" the",
		" answer",
		" depends",
		" on",
		" context.",
		" Let",
		" me",
		" explain",
		" with",
		" a",
		" few",
		" key",
		" points:",
		"\n\n",
		"1.",
		" First,",
		" consider",
		" the",
		" underlying",
		" assumptions.",
		"\n",
		"2.",
		" Then,",
		" evaluate",
		" the",
		" available",
		" evidence.",
		"\n",
		"3.",
		" Finally,",
		" draw",
		" your",
		" own",
		" conclusions.",
		"\n\n",
		"Hope",
		" that",
		" helps!",
	];
	for (const word of words) {
		yield word;
		await new Promise((r) => setTimeout(r, 20 + Math.random() * 60));
	}
}
